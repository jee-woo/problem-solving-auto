def solution(n):
    # 3진법의 나머지 (0, 1, 2)를 124 나라의 숫자 (4, 1, 2)로 매핑
    # 인덱스 0은 나머지가 0일 때 (4), 인덱스 1은 나머지 1일 때 (1), 인덱스 2는 나머지 2일 때 (2)
    otf = ['4', '1', '2'] 
    answer = ''
    
    while n > 0:
        # 1. 나머지 계산: n을 3으로 나눈 나머지
        # 나머지가 1이면 otf[1] = '1'
        # 나머지가 2이면 otf[2] = '2'
        # 나머지가 0이면 otf[0] = '4'
        remainder = n % 3
        
        # 2. 몫 계산 (다음 n)
        # 나머지가 0인 경우(특수 상황)에는 n을 1 감소시킨 후 3으로 나눠야 함.
        # 이를 일반화한 수식: (n - 1) // 3
        n = (n - 1) // 3
        
        # 3. 결과에 추가 (3진법처럼 낮은 자리부터 추가)
        answer = otf[remainder] + answer
        
    return answer