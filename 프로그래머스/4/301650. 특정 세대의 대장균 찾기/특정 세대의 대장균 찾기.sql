With ROOT AS (
    SELECT ID
    FROM ECOLI_DATA
    WHERE PARENT_ID IS NULL
),
SECOND AS (
    SELECT ID
    FROM ECOLI_DATA
    WHERE PARENT_ID in (SELECT ID FROM ROOT)
),
THIRD AS (
    SELECT ID
    FROM ECOLI_DATA
    WHERE PARENT_ID in (SELECT ID FROM SECOND)
)

SELECT ID
FROM THIRD
ORDER BY ID

