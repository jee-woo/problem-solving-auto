def solution(n, times):
    answer = 0
    
    MAX = max(times) * n
    
    l = 0
    r = MAX
    
    while l < r:
        mid = (l + r) // 2
        count = 0
        for t in times:
            count += mid // t
        if count >= n:
            r = int((l + r) / 2)
        else:
            l = int((l + r) / 2) + 1
    
    return r

